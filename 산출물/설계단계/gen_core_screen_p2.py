def set_cell_bg(cell, hex_color):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)

def add_heading(doc, text, level=1, color='1A73E8'):
    p = doc.add_heading(text, level=level)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    for run in p.runs:
        run.font.color.rgb = RGBColor(*bytes.fromhex(color))
    return p

def add_para(doc, text='', bold=False, size=10, color=None):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold; run.font.size = Pt(size)
    if color:
        run.font.color.rgb = RGBColor(*bytes.fromhex(color))
    return p

def add_ascii(doc, ascii_text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after = Pt(4)
    pPr = p._p.get_or_add_pPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'),'clear'); shd.set(qn('w:color'),'auto')
    shd.set(qn('w:fill'),'F0F4FF')
    pPr.append(shd)
    run = p.add_run(ascii_text)
    run.font.name = 'Courier New'; run.font.size = Pt(7.5)
    run.font.color.rgb = RGBColor(0x20,0x20,0x20)
    return p

def add_table(doc, headers, rows, hc='34495E'):
    table = doc.add_table(rows=1, cols=len(headers))
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.LEFT
    hdr = table.rows[0]
    for i,h in enumerate(headers):
        c = hdr.cells[i]
        c.text = h
        c.paragraphs[0].runs[0].bold = True
        c.paragraphs[0].runs[0].font.size = Pt(9)
        c.paragraphs[0].runs[0].font.color.rgb = RGBColor(0xFF,0xFF,0xFF)
        set_cell_bg(c, hc)
    for rd in rows:
        row = table.add_row()
        for i,val in enumerate(rd):
            c = row.cells[i]
            c.text = str(val)
            c.paragraphs[0].runs[0].font.size = Pt(9)
    return table

def screen_block(doc, sid, sname, stype, access, desc,
                 ascii_art, comp_rows, event_rows, valid_rows):
    doc.add_paragraph()
    t = doc.add_table(rows=2, cols=4)
    t.style = 'Table Grid'
    for i,(k,v) in enumerate([('화면 ID',sid),('화면명',sname),('화면유형',stype),('접근권한',access)]):
        c = t.rows[0].cells[i]; p=c.paragraphs[0]; p.clear()
        r1=p.add_run(k+': '); r1.bold=True; r1.font.size=Pt(9)
        r2=p.add_run(v);       r2.font.size=Pt(9)
        set_cell_bg(c,'E8F0FE')
    merged = t.rows[1].cells[0].merge(t.rows[1].cells[3])
    merged.text = '화면 설명: ' + desc
    merged.paragraphs[0].runs[0].font.size = Pt(9)

    add_para(doc,'[ 화면 목업 ]',bold=True,size=9,color='1A73E8')
    add_ascii(doc, ascii_art)

    add_para(doc,'[ 주요 컴포넌트 ]',bold=True,size=9,color='1A73E8')
    add_table(doc,['컴포넌트명','유형','기능 설명'], comp_rows)

    add_para(doc,'[ 이벤트 처리 ]',bold=True,size=9,color='1A73E8')
    add_table(doc,['이벤트','트리거','처리 내용'], event_rows)

    add_para(doc,'[ 유효성 검사 규칙 ]',bold=True,size=9,color='1A73E8')
    add_table(doc,['필드명','규칙','오류 메시지'], valid_rows)

    doc.add_paragraph()
    p = doc.add_paragraph('─'*90)
    p.runs[0].font.size = Pt(7)
    p.runs[0].font.color.rgb = RGBColor(0xCC,0xCC,0xCC)


