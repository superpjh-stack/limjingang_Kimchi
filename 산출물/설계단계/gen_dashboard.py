def set_cell_bg(cell, rgb):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), str(rgb))
    tcPr.append(shd)

def heading(doc, text, level=1):
    p = doc.add_paragraph(text, style=f"Heading {level}")
    return p

def add_mono(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = "Courier New"
    run.font.size = Pt(8)
    p.paragraph_format.space_before = Pt(2)
    p.paragraph_format.space_after = Pt(2)
    return p

def screen_section(doc, scr_id, scr_name, scr_type, permission, desc, mockup, components, events):
    doc.add_paragraph()
    tbl = doc.add_table(rows=1, cols=4)
    tbl.style = "Table Grid"
    hdrs = ["화면 ID", "화면명", "화면 유형", "접근 권한"]
    vals = [scr_id, scr_name, scr_type, permission]
    for i, cell in enumerate(tbl.rows[0].cells):
        cell.text = hdrs[i]
        cell.paragraphs[0].runs[0].bold = True
        set_cell_bg(cell, RGBColor(0x2E,0x75,0xB6))
        cell.paragraphs[0].runs[0].font.color.rgb = RGBColor(0xFF,0xFF,0xFF)
    tbl2 = doc.add_table(rows=1, cols=4)
    tbl2.style = "Table Grid"
    for i, cell in enumerate(tbl2.rows[0].cells):
        cell.text = vals[i]

    doc.add_paragraph(f"▶ 화면 설명: {desc}")

    doc.add_paragraph("[ 화면 목업 (ASCII) ]").runs[0].bold = True
    add_mono(doc, mockup)

    if components:
        doc.add_paragraph("[ 주요 컴포넌트 ]").runs[0].bold = True
        ctbl = doc.add_table(rows=1, cols=3)
        ctbl.style = "Table Grid"
